bucket_list = ["climb Mt. Everest", "eat fruits from a tree that I planted"]

# Add an element
bucket_list.append("sail across the Atlantic ocean")
print(bucket_list)  # OUTPUT: ["climb Mt. Everest", "eat fruits from a tree that I planted", "sail across the Atlantic ocean"]

# Remove an element
bucket_list.pop()
print(bucket_list)  # OUTPUT: ["climb Mt. Everest", "eat fruits from a tree that I planted"]