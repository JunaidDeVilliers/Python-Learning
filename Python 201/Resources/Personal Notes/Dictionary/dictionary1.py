# You can create a dictionary using curly braces ({}) and adding keys and values that are separated 
# by a colon (:). Multiple entries are separated by commas (,), just like elements in collections:

my_dict = {"greeting": "hello", "name": "martin"}

# The dictionary my_dict has two items in it:

#     "greeting": "hello"
#     "name": "martin"

# Each of these two items consists of a key and a value. If you take apart the first element, 
# then you'll see that it consists of:

#     key: "greeting"
#     value: "hello"

# Dictionaries are different from collections you've encountered before in that you access their 
# values not by index, but by through a unique key. This also means that you can't have duplicate 
# keys in a Python dictionary. There can, however, be many duplicate values in every dictionary.