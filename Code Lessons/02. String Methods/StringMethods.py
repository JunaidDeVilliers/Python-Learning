name = "Junaid"

# Prints the  length of the name
print(len(name))

# Prints the first index of the letter you are looking for
print(name.find("d"))

# Capitalizes the first letter of the variable
print(name.capitalize())

# Converts everything to uppercase
print(name.upper())

# Converts everything to lowercase
print(name.lower())

# Checks if it is a digit
print(name.isdigit())

# Checks if it is alphabet characters. Beware of spaces
print(name.isalpha())

# Counts the amount of the given letter
print(name.count("u"))

# Replaces the first letter with the second one (a with o)
print(name.replace("a", "o"))

# Prints the name 3 times
print(name*3)