import math

pi = 3.14
x = 1
y = 2
z = 3

# rounding to the nearest whole integer
print(round(pi))

# rounding up
print(math.ceil(pi))

#rounding down
print(math.floor(pi))

# The absolute value a real number is the non-negative value of the number without regard to its sign. 
# tells you how far away a number is from 0
print(abs(-5))

# raise a base number to a power. (base, exponent)
print(pow(3, 2))

# square root
print(math.sqrt(81))

# Max function will find the largest of a varyring amount of values
print(max(x, y, z))

# Min function will find the lowest of a varyring amount of values
print(min(x, y, z))